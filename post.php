            psychedelic 1960s with its black light posters, new research shows.
            The yellowish pigments that give the visible glow are called betaxanthins. These pigments
            show up in four-o-clocks (Mirabilis jalapa), portulacas, and certain other flashy flowers
            related to carnations, cacti, and bougainvilleas. Previous research on the pigments missed
