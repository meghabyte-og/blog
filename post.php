            his colleagues report that blue light excites betaxanthin pigments to glow yellowish green.
            In the Sept. 15 Nature, the researchers report that four-o’clocks have both fluorescent and
            antifluorescent pigments. The parts of the flower that look yellow are also emitting green
            fluorescent light. Other parts of the flower contain both betaxanthin and a violet pigment,
            betanin, which absorbs most of the fluorescence emissions. The combination of these pigments
            might create patterns that attract pollinators, Gandía-Herrero speculates.</p>       
